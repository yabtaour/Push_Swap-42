#include "push_swap.h"

int ft_check_imposter(char *args)
{
    int i;

    i = 0;
    while (args[i] != '\0')
    {
        if (args[i] != ' ')
        {
            if ((args[i] > '9' || args[i] < '0' || args[i] != '-'))
                return (1);
        }
        i++;
    }
    return (0);
}